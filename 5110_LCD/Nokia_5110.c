void LCD_WriteLine(uint8_t* x, uint8_t center){
  if(center){
    int i = 0, offset = 0, g_i = 0, half = 0;
    while(x[i] != '\0'){
      i++;
    }

    char buf1[14], buf2[14];
    if(i > 14){
      strncpy(buf1, x, 14);
      LCD_WriteLine(buf1, 1);
      strncpy(buf2, x + 14, i - 14);
      LCD_WriteLine(buf2, 1);
      return;
    }

    // Write spaces to center the text
    if((i % 2) != 0){
      half = 1;
      LCD_SendByte(0x00);
      LCD_SendByte(0x00);
      LCD_SendByte(0x00);
    }
    offset = (14-i)/2;
    for(i = 0; i < offset; i++){
      LCD_Letter(' ');
      g_i++;
    }

    // Write the text
    i = 0;
    while(x[i] != '\0'){
      LCD_Letter(x[i]);
      i++;
      g_i++;
    }

    // Write spaces to center the text
    if(!half){
      for(i = g_i; i < 14; i++){
        LCD_Letter(' ');
      }
    } else {
      for(i = g_i; i < 13; i++){
        LCD_Letter(' ');
      }
      LCD_SendByte(0x00);
      LCD_SendByte(0x00);
      LCD_SendByte(0x00);
    }
  } else {
    int i = 0;
    while(x[i] != '\0'){
      LCD_Letter(x[i]);
      i++;
    }
    while(i < 14){
      LCD_Letter(' ');
      i++;
    }
  }

}

/**
  * @brief  Send byte through SPI.
  * @param  sendbyte: byte to be transmitted via SPI.
  * @retval None
  */
uint8_t LCD_SendByte(uint8_t sendbyte) {

	uint8_t readbyte;

	HAL_SPI_TransmitReceive(&SpiHandle, &sendbyte, &readbyte, 1, 10);
	// Return the Byte read from the SPI bus
	return readbyte;
}

